#   range demo

# generate number list: 0, 1, 2, 3, 4
num1 = range(5)
print(f"The output of range(5) is : {list(num1)}")

# generate number list: # 2, 3, 4, 5, 6, 7
num2 = range(2, 8)
print(f"The output of range(2, 8) is : {list(num2)}")

# generate number list: # 0, 2, 4, 6, 8
num3 = range(0, 10, 2)
print(f"The output of range(0, 10, 2) is : {list(num3)}")
